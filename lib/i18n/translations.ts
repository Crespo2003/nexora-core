export type TranslationKey = keyof typeof translations.en;

export function getTranslations(language: Language) {
  return translations[language] ?? translations[defaultLanguage];
}

export function translateKnownMessage(message: string, language: Language): string {
  const target = translations[language];

  for (const group of ['notices', 'errors'] as const) {
    const enMessages = translations.en[group];
    const zhMessages = translations.zh[group];
    const targetMessages = target[group];

    for (const key of Object.keys(enMessages) as Array<keyof typeof enMessages>) {
      if (message === enMessages[key] || message === zhMessages[key]) {
        return targetMessages[key];
      }
    }
  }

  return message;
}
